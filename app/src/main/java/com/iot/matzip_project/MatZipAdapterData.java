package com.iot.matzip_project;

public class MatZipAdapterData {
    private String STORENAME;
    private String COMMENT;
    private String CATEGORY;
    private String MENU;
    private String MAINIMAGE;
    private String SOJUIMAGE;
    private String BEERIMAGE;


    public String getSTORENAME() {
        return STORENAME;
    }

    public void setSTORENAME(String STORENAME) {
        this.STORENAME = STORENAME;
    }

    public String getCOMMENT() {
        return COMMENT;
    }

    public void setCOMMENT(String COMMENT) {
        this.COMMENT = COMMENT;
    }

    public String getCATEGORY() {
        return CATEGORY;
    }

    public void setCATEGORY(String CATEGORY) {
        this.CATEGORY = CATEGORY;
    }

    public String getMAINIMAGE() {
        return MAINIMAGE;
    }

    public void setMAINIMAGE(String MAINIMAGE) {
        this.MAINIMAGE = MAINIMAGE;
    }

    public String getSOJUIMAGE() {
        return SOJUIMAGE;
    }

    public void setSOJUIMAGE(String SOJUIMAGE) {
        this.SOJUIMAGE = SOJUIMAGE;
    }

    public String getBEERIMAGE() {
        return BEERIMAGE;
    }

    public void setBEERIMAGE(String BEERIMAGE) {
        this.BEERIMAGE = BEERIMAGE;
    }

    public String getMENU() {
        return MENU;
    }

    public void setMENU(String MENU) {
        this.MENU = MENU;
    }
}
